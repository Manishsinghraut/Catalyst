package com.lucernex.qa.data;

/*
Used to verfiy Single BP Schedule grid
 */

public class SingleBPSchedule {
    private String salesGroup;
    private String monthYear;
    private String grossSales;
    private String excludedSales;
    private String leaseYTDSales;
    private String breakpoint;
    private String salesPastBreakpoint;
    private String rate;
    private String totalPercentRent;
    private String amountPaid;
    private String percentOwed;


    public String getGroup() {
        return salesGroup;
    }

    public void setGroup(String SalesGroup) {
        this.salesGroup = salesGroup;
    }

    public String getMonthYear() {
        return monthYear;
    }

    public void setMonthYear(String monthYear) {
        this.monthYear = monthYear;
    }

    public String getGrossSales() {
        return grossSales;
    }

    public void grossSales(String grossSales) {
        this.grossSales = grossSales;
    }

    public String getExcludedSales() {
        return excludedSales;
    }

    public void setExcludedSales(String excludedSales) {
        this.excludedSales = excludedSales;
    }

    public String getLeaseYTDSales() {
        return leaseYTDSales;
    }

    public void setLeaseYTDSales(String leaseYTDSales) {
        this.leaseYTDSales = leaseYTDSales;
    }

    public String getBreakpoint() {
        return breakpoint;
    }

    public void setBreakpoint(String breakpoint) {
        this.breakpoint = breakpoint;
    }

    public String getSalesPastBreakpoint() {
        return salesPastBreakpoint;
    }

    public void setSalesPastBreakpoint(String salesPastBreakpoint) {
        this.salesPastBreakpoint = salesPastBreakpoint;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getTotalPercentRent() {
        return totalPercentRent;
    }

    public void setTotalPercentRent(String totalPercentRent) {
        this.totalPercentRent = totalPercentRent;
    }

    public String getAmountPaid() {
        return amountPaid;
    }

    public void setAmountPaid(String amountPaid) {
        this.amountPaid = amountPaid;
    }

    public String getPercentOwed() {
        return percentOwed;
    }

    public void setPercentOwed(String percentOwed) {
        this.percentOwed = percentOwed;
    }

    public SingleBPSchedule(String salesGroup, String salesType, String grossSales, String excludedSales, String leaseYTDSales,
                            String breakpoint, String salesPastBreakpoint, String rate, String totalPercentRent, String amountPaid, String percentOwed) {
        this.salesGroup = salesGroup;
        this.monthYear = monthYear;
        this.grossSales = grossSales;
        this.excludedSales = excludedSales;
        this.leaseYTDSales = leaseYTDSales;
        this.breakpoint = breakpoint;
        this.salesPastBreakpoint = salesPastBreakpoint;
        this.rate = rate;
        this.totalPercentRent = totalPercentRent;
        this.amountPaid = amountPaid;
        this.percentOwed = percentOwed;

    }

    @Override
    public String toString() {
        return "Sales{" +
                "salesGroup='" + salesGroup + '\'' +
                ",monthYear='" + monthYear + '\'' +
                ",grossSales='" + grossSales + '\'' +
                ",excludedSales='" + excludedSales + '\'' +
                ",leaseYTDSales='" + leaseYTDSales + '\'' +
                ",breakpoint='" + breakpoint + '\'' +
                ",salesPastBreakpoint='" + salesPastBreakpoint + '\'' +
                ",rate='" + rate + '\'' +
                ",totalPercentRent='" + totalPercentRent + '\'' +
                ",amountPaid='" + amountPaid + '\'' +
                ",percentOwed='" + percentOwed + '\'' +
                '}';
    }

    public SingleBPSchedule() {
    }
}
