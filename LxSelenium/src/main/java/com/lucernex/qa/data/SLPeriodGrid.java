package com.lucernex.qa.data;

/**
 * Used to verify values to fields in SL Period Grid
 */
public class SLPeriodGrid {
    private String year;
    private String period;
    private String periodLength;
    private String cashRent;
    private String rentExpense;
    private String deferredRent;
    private String cumulativeBalance;
    private String status;
    private String beginDate;
    private String endDate;
    private String blank;

    public String getYear() {  return year;  }
    public void setYear(String year) {
        this.year = year;
    }

    public String getPeriod() {   return period;  }
    public void setPeriod(String period) {   this.period = period;  }

    public String getPeriodLength() {    return periodLength;   }
    public void setPeriodLength(String periodLength) {
        this.periodLength = periodLength;
    }

    public String getCashRent() {   return cashRent;  }
    public void setCashRent(String cashRent){this.cashRent = cashRent;}

    public String getRentExpense() {   return rentExpense;  }
    public void setRentExpense(String rentExpense){this.rentExpense = rentExpense;}

    public String getDeferredRent() {   return deferredRent;  }
    public void setDeferredRent(String deferredRent){this.deferredRent = deferredRent;}

    public String getCumulativeBalance() {   return cumulativeBalance;  }
    public void setCumulativeBalance(String cumulativeBalance){this.cumulativeBalance = cumulativeBalance;}

    public String getStatus() {   return status;  }
    public void setStatus(String status){this.status = status;}

    public String getBeginDate() {   return beginDate;  }
    public void setBeginDate(String beginDate){this.beginDate = beginDate;}

    public String getEndDate() {   return endDate;  }
    public void setEndDate(String endDate){this.endDate = endDate;}

    public String getBlank() {  return blank;  }
    public void setBlank(String blank) {
        this.blank = blank;
    }


    public SLPeriodGrid(String year, String period, String periodLength, String cashRent, String rentExpense,
                        String deferredRent, String cumulativeBalance, String status,String beginDate, String endDate,String blank) {
        this.year = year;
        this.period = period;
        this.periodLength = periodLength;
        this.cashRent = cashRent;
        this.rentExpense = rentExpense;
        this.deferredRent = deferredRent;
        this.cumulativeBalance = cumulativeBalance;
        this.status = status;
        this.beginDate = beginDate;
        this.endDate = endDate;
        this.blank = blank;

    }

    @Override
    public String toString() {
        return "SLPeriodGrid{" +
                "year='" + year + '\'' +
                ",period='" + period + '\'' +
                ",periodLength='" + periodLength + '\'' +
                ",cashRent='" + cashRent + '\'' +
                ",rentExpense='" + rentExpense + '\'' +
                ",deferredRent='" + deferredRent + '\'' +
                ",cumulativeBalance='" + cumulativeBalance + '\'' +
                ",status='" + status + '\'' +
                ",beginDate='" + beginDate + '\'' +
                ",endDate='" + endDate + '\'' +
                ",blank='" + blank + '\'' +
                '}';
    }

    public SLPeriodGrid() {
    }
}
