package com.lucernex.qa.datamart;

import org.openqa.selenium.WebElement;
import org.openqa.selenium.support.FindBy;
import org.openqa.selenium.support.How;

/**
 * Represents the Datamart toolbar which has these buttons: [Dashboard,
 * Create Mapping, and Log Events].
 */
public class Toolbar {
    @FindBy(how = How.CSS, using = "div#dashboardBtn button")
    public WebElement dashboardBtn;

    @FindBy(how = How.CSS, using = "div#createMappingBtn button")
    public WebElement createMappingBtn;

    @FindBy(how = How.CSS, using = "div#logEventBtn button")
    public WebElement logEventsBtn;
}
